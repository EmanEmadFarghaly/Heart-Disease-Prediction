# =========================
# 📄 PDF GENERATOR FUNCTION
# =========================
def create_pdf(input_data, prob, cp_type):
    pdf = FPDF()
    pdf.add_page()
    pdf.set_font("Arial", 'B', 16)
    pdf.cell(200, 10, txt="Heart Disease Predictor - Diagnostic Report", ln=True, align='C')
    
    pdf.set_font("Arial", size=12)
    pdf.ln(10)
    pdf.cell(200, 10, txt=f"Risk Probability: {prob*100:.2f}%", ln=True)
    pdf.cell(200, 10, txt=f"Chest Pain Type: {cp_type}", ln=True)
    pdf.ln(5)
    
    pdf.set_font("Arial", 'B', 12)
    pdf.cell(200, 10, txt="Clinical Metrics Input:", ln=True)
    pdf.set_font("Arial", size=10)
    for col in input_data.columns:
        pdf.cell(200, 8, txt=f"{col.upper()}: {input_data[col].values[0]}", ln=True)
    
    pdf.ln(10)
    pdf.set_font("Arial", 'I', 8)
    pdf.multi_cell(0, 5, "DISCLAIMER: This report is generated by an AI model trained on the UCI Heart Disease dataset. It is for educational purposes and does not constitute a medical diagnosis.")
    
    return pdf.output(dest='S').encode('latin-1')

